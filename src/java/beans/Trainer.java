package beans;

public class Trainer {
   
private int TrainerId;
private String TrainerName;
private String Gender;
private String Country;
private String EMailId;
private String Occupation;
private String WorkingOrganisation;
private String Username;
private String Password;
private String SecurityQuestion;
private String SecurityAnswer;
private String ProfilePicture;

    public String getProfilePicture() {
        return ProfilePicture;
    }

    public void setProfilePicture(String ProfilePicture) {
        this.ProfilePicture = ProfilePicture;
    }

    public int getTrainerId() {
        return TrainerId;
    }

    public void setTrainerId(int TrainerId) {
        this.TrainerId = TrainerId;
    }

    public String getTrainerName() {
        return TrainerName;
    }

    public void setTrainerName(String TrainerName) {
        this.TrainerName = TrainerName;
    }

    public String getGender() {
        return Gender;
    }

    public void setGender(String Gender) {
        this.Gender = Gender;
    }

    public String getCountry() {
        return Country;
    }

    public void setCountry(String Country) {
        this.Country = Country;
    }

    public String getEMailId() {
        return EMailId;
    }

    public void setEMailId(String EMailId) {
        this.EMailId = EMailId;
    }

    public String getOccupation() {
        return Occupation;
    }

    public void setOccupation(String Occupation) {
        this.Occupation = Occupation;
    }

    public String getWorkingOrganisation() {
        return WorkingOrganisation;
    }

    public void setWorkingOrganisation(String WorkingOrganisation) {
        this.WorkingOrganisation = WorkingOrganisation;
    }

    public String getUsername() {
        return Username;
    }

    public void setUsername(String Username) {
        this.Username = Username;
    }

    public String getPassword() {
        return Password;
    }

    public void setPassword(String Password) {
        this.Password = Password;
    }

    public String getSecurityQuestion() {
        return SecurityQuestion;
    }

    public void setSecurityQuestion(String SecurityQuestion) {
        this.SecurityQuestion = SecurityQuestion;
    }

    public String getSecurityAnswer() {
        return SecurityAnswer;
    }

    public void setSecurityAnswer(String SecurityAnswer) {
        this.SecurityAnswer = SecurityAnswer;
    }

}
